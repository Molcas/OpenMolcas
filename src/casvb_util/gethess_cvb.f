      subroutine gethess_cvb(hess)
      implicit real*8 (a-h,o-z)
#include "ext_cvb.fh"
#include "main_cvb.fh"
#include "optze_cvb.fh"
#include "files_cvb.fh"
#include "print_cvb.fh"

      dimension hess(nfr,nfr)

      call mxunit_cvb(hess,nfr)
      do 100 ivar=1,nfr
      call hess_cvb(hess(1,ivar))
100   continue
      return
      end
