      an(27, 4) =   -2.0_wp /    1155.0_wp  !  -2 / 3*5*7*11
      an(27, 6) =    4.0_wp /   27027.0_wp  !   4 / 27*7*11*13
!---------  Lr  -------------------------------!
      an(28, 2) =    0.0_wp
      an(28, 4) =    0.0_wp
      an(28, 6) =    0.0_wp
!---------  End -------------------------------!

      Do i=1,6
        a(i)=an(ln,i)
      End Do

      Return
      End subroutine set_an
