      an( 5, 6) =    0.0_wp
!---------  Eu  -------------------------------!
      an( 6, 2) =    0.0_wp
      an( 6, 4) =    0.0_wp
      an( 6, 6) =    0.0_wp
!---------  Gd  -------------------------------!
      an( 7, 2) =    0.0_wp
      an( 7, 4) =    0.0_wp
      an( 7, 6) =    0.0_wp
!---------  Tb  -------------------------------!
