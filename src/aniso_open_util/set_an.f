      an(13, 6) =    4.0_wp /   27027.0_wp  !   4 / 27*7*11*13
!---------  Lu  -------------------------------!
      an(14, 2) =    0.0_wp
      an(14, 4) =    0.0_wp
      an(14, 6) =    0.0_wp
